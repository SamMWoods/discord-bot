import dotenv from "dotenv";

dotenv.config();

const { DISCORD_TOKEN, DISCORD_CLIENT_ID, DISCORD_GUILD_ID, LAVALINK_HOST, LAVALINK_PORT, LAVALINK_PASSWORD, LAVALINK_YT_REFRESH_TOKEN } = process.env;

if (!DISCORD_TOKEN || !DISCORD_CLIENT_ID || !DISCORD_GUILD_ID || !LAVALINK_HOST || !LAVALINK_PORT || !LAVALINK_PASSWORD || !LAVALINK_YT_REFRESH_TOKEN) {
  throw new Error("Missing environment variables");
}

export const config = {
  DISCORD_TOKEN,
  DISCORD_CLIENT_ID,
  DISCORD_GUILD_ID,
  LAVALINK_HOST,
  LAVALINK_PORT: Number(LAVALINK_PORT),
  LAVALINK_PASSWORD,
  LAVALINK_YT_REFRESH_TOKEN,
};